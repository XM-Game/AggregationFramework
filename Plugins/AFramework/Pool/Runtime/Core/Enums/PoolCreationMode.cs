namespace AFramework.Pool
{
    /// <summary>
    /// 对象池创建模式枚举
    /// </summary>
    public enum PoolCreationMode
    {
        /// <summary>
        /// 懒加载模式
        /// 按需创建对象，不预热
        /// </summary>
        /// <remarks>
        /// 适用场景：
        /// - 启动时间敏感
        /// - 对象使用频率低
        /// - 内存优先
        /// 
        /// 优点：启动快，内存占用低
        /// 缺点：首次使用时有创建开销
        /// </remarks>
        Lazy = 0,

        /// <summary>
        /// 预热模式
        /// 启动时预先创建对象
        /// </summary>
        /// <remarks>
        /// 适用场景：
        /// - 运行时性能敏感
        /// - 对象使用频率高
        /// - 性能优先
        /// 
        /// 优点：运行时无创建开销
        /// 缺点：启动时间长，内存占用高
        /// </remarks>
        Warmup = 1,

        /// <summary>
        /// 按需模式
        /// 根据实际使用情况动态创建
        /// </summary>
        /// <remarks>
        /// 适用场景：
        /// - 负载波动大
        /// - 对象数量不可预测
        /// - 平衡启动和运行时性能
        /// 
        /// 优点：灵活适应需求
        /// 缺点：可能有运行时创建开销
        /// </remarks>
        OnDemand = 2,
    }
}
