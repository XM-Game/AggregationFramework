namespace AFramework.Pool
{
    /// <summary>
    /// 对象池容量模式枚举
    /// </summary>
    public enum PoolCapacityMode
    {
        /// <summary>
        /// 固定容量模式
        /// 池容量固定，达到上限后无法创建新对象
        /// </summary>
        /// <remarks>
        /// 适用场景：
        /// - 内存敏感场景，需要严格控制内存占用
        /// - 对象数量可预测
        /// - 防止内存泄漏
        /// </remarks>
        Fixed = 0,

        /// <summary>
        /// 动态容量模式
        /// 池容量可动态扩展，按倍数增长
        /// </summary>
        /// <remarks>
        /// 适用场景：
        /// - 负载波动场景，自动适应需求
        /// - 对象数量不可预测
        /// - 性能优先于内存
        /// </remarks>
        Dynamic = 1,

        /// <summary>
        /// 无界容量模式
        /// 池容量无限制，可无限增长
        /// </summary>
        /// <remarks>
        /// 适用场景：
        /// - 开发调试阶段
        /// - 不关心内存占用
        /// - 临时测试
        /// 
        /// 警告：生产环境慎用，可能导致内存泄漏
        /// </remarks>
        Unbounded = 2,
    }
}
